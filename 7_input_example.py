# 7_input_example.py
# This program explains how to take input from the user in Python.
# It shows how to read input and convert it into the required data type.

print("USER INPUT EXAMPLE\n")

# Taking name input from the user (input is always taken as string)
name = input("Enter your name: ")

# Taking age input from the user
# int() is used to convert string input into integer
age = int(input("Enter your age: "))

# Displaying the entered details
print("\nUser Details:")
print("Name:", name)
print("Age:", age)
